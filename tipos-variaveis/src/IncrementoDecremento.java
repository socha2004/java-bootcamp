public class IncrementoDecremento{
    public static void main(String[] args) {
        int numero = 5;

        // Primeiro exibe o valor da variável, depois incrementa (operador pós-incremento)
        System.out.println(numero++);

        // Primeiro incrementa a variável e depois exibe ela (operador pré-incremento)
        System.out.println(++numero);


    }
}