public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        // VARIAVEIS

        // <tipoDaVariavel> <nomeDaVariavel> = <valorAtribuidoOpcional>
        int anoFabricacao = 2010;
        double salarioMinimo = 2500.547;

        // Variaveis do tipo float precisam do F no final do valor
        float pi = 3.14F;

        // CONSTANTES

        // Constantes precisam estar em caixa alta, e antes da declarção do tipo 
        // vem a palavra chave "final"

        final double VALOR_DE_PI = 3.14;
        

    }
}
